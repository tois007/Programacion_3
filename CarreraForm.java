    private void inicializarCarrera() {
        vehiculos = new ArrayList<>();
        for (int i = 0; i < NUM_VEHICULOS; i++) {
            vehiculos.add(new Vehiculo(i));
            jComboBoxApuesta.addItem("Vehículo " + (i + 1));
        }

        // Timer que actualiza la carrera
        timer = new javax.swing.Timer(20, e -> tick());
    }
    private void tick() {
        if (!corriendo) return;

        double factor = jSliderVelocidad.getValue() / 100.0;
        double delta = timer.getDelay() / 1000.0 * factor;
        tiempoSim += delta;

        int metaX = jPanelPista.getWidth() - 40;

        for (Vehiculo v : vehiculos) {
            if (!v.terminado) {
                v.x += v.velocidad * delta;
                if (v.x >= metaX) {
                    v.terminado = true;
                    v.tiempoFinal = tiempoSim;
                    terminados++;
                }
            }
        }

        jPanelPista.repaint();

        if (terminados >= NUM_VEHICULOS) {
            timer.stop();
            corriendo = false;
            mostrarResultados();
        }
    }
    private void mostrarResultados() {
        List<Vehiculo> ordenados = new ArrayList<>(vehiculos);
        ordenados.sort(Comparator.comparingDouble(v -> v.tiempoFinal));

        StringBuilder sb = new StringBuilder("Resultados:\n");
        for (int i = 0; i < ordenados.size(); i++) {
            Vehiculo v = ordenados.get(i);
            sb.append((i + 1)).append(") Vehículo ").append(v.id + 1)
              .append(" - ").append(String.format("%.2f s", v.tiempoFinal)).append("\n");
        }

        String ganador = "Ganador: Vehículo " + (ordenados.get(0).id + 1);
        if (apuesta >= 0) {
            if (apuesta == ordenados.get(0).id) {
                ganador += "\n¡Tu apuesta ganó!";
            } else {
                ganador += "\nTu apuesta perdió.";
            }
        }

        JOptionPane.showMessageDialog(this, sb.toString() + "\n" + ganador);
    }
    
    

