    private void jButtonIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIniciarActionPerformed
        if (corriendo) return;

        Random rnd = new Random();
        int alto = jPanelPista.getHeight();
        for (int i = 0; i < vehiculos.size(); i++) {
            Vehiculo v = vehiculos.get(i);
            v.x = 10;
            v.y = 30 + i * (alto / NUM_VEHICULOS);
            v.velocidad = 50 + rnd.nextDouble() * 150;
            v.terminado = false;
            v.tiempoFinal = Double.POSITIVE_INFINITY;
        }

        tiempoSim = 0;
        terminados = 0;
        corriendo = true;
        timer.start();
        jLabelEstado.setText("¡Carrera en curso!");
    }//GEN-LAST:event_jButtonIniciarActionPerformed

    private void jButtonReiniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonReiniciarActionPerformed
        timer.stop();
        corriendo = false;
        tiempoSim = 0;
        terminados = 0;
        apuesta = -1;
        jLabelEstado.setText("Listo para apostar.");
        jPanelPista.repaint();
    }//GEN-LAST:event_jButtonReiniciarActionPerformed

